                }
            });
            $('.add-to-cart').click(function() {
                var url = 'shop/orders';
                var data = {
                    'book_id': $(this).data('book-id')
                };
                $.ajax({
                    url: url,
                    type: 'POST',
                    data: data,
                    success: function(result) {
                        $('.cart-number').text(result.quantity);
                        alert('Order success!');
                    },
                    error: function() {
                        alert('Some went wrong!');
                        location.reload();
                    }
                });
            });
        });
